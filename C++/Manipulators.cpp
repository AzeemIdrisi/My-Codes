// Manipulators are used for formatting the output
#include <iostream>
using namespace std;

int main()
{
    // endl
    cout << 163 << endl;

    // hex
    cout << hex << 163 << endl;

    // oct
    cout << oct << 163 << endl;

    // fixed
    cout << fixed << 163.6554 << endl;

    // scientific
    cout << scientific << 163.6554 << endl;
}